wstring get_male_patronymic(){
    auto s = male_names[rand() % 733];
    auto last_literal = s[s.length() - 1];
    if (last_literal == L'ь' or last_literal == L'й') {
        return wstring(s, 0, s.length() - 1) + L"евич";
    }
    else if (last_literal == L'а' or last_literal == L'е' or
            last_literal == L'ё' or last_literal == L'и' or
            last_literal == L'о' or last_literal == L'у' or
            last_literal == L'ы' or last_literal == L'э' or
            last_literal == L'ю' or last_literal == L'я'){
        return wstring(s, 0, s.length() - 1) + L"ич";
    }
    else return s + L"ович";
}

